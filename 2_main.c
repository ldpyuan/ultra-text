#include <stdio.h>

int main() {
    // 1. 定义包含 5 个整数的数组
    int arr[5];
    int i;
    int *p = arr;  // 指针 p 指向数组首元素（数组名 arr 本质是首元素地址）

    // 2. 键盘输入数组元素
    printf("请输入 5 个整数，以空格或回车分隔：\n");
    for (i = 0; i < 5; i++) {
        // 方式1：通过数组下标输入（对照用）
        // scanf("%d", &arr[i]);
        // 方式2：通过指针输入（指针移动指向当前元素）
        scanf("%d", p + i);  // p+i 等价于 &arr[i]，指向第 i 个元素
    }

    // 3. 通过指针将所有元素乘以 2
    printf("\n处理：将数组所有元素乘以 2\n");
    for (i = 0; i < 5; i++) {
        // 方式1：指针解引用 + 偏移（推荐，清晰）
        *(p + i) *= 2;  // *(p+i) 等价于 arr[i]，对其乘以 2
        // 方式2：移动指针（循环中需注意指针位置，避免越界）
        // *p *= 2;
        // p++;  // 指针向后移动 1 个 int 长度（4字节）
    }

    // 4. 打印处理后的数组（验证结果）
    printf("处理后的数组：\n");
    // 重置指针到数组首元素（若使用方式2移动过指针，必须执行此步）
    p = arr;
    for (i = 0; i < 5; i++) {
        // 通过指针输出
        printf("%d ", *(p + i));
    }
    printf("\n");

    return 0;
}
